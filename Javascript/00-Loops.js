// While Loop
var count = 1;
while (count < 6){
	console.log("count: " + count);
	count++;
}
var str = "hello";
var count = 0;
while(count < str.length) {
	console.log(str[count]);
	count++;
}
for(var count = 0; count < 6; count++){
	console.log(count);
}
